    void ObtenIP() {

        try {
            String[] str = new String[2];

            BufferedReader ent = new BufferedReader(new FileReader("ip.csv"));
            StringTokenizer stt;
            String linea = ent.readLine();

            while (linea != null) {

                stt = new StringTokenizer(linea, ";");
                str[0] = stt.nextToken();
                str[1] = stt.nextToken();

                linea = ent.readLine();
                pru.add(new ArrayList());
                pru.get(pru.size() - 1).add(str[0]);
                pru.get(pru.size() - 1).add(str[1]);
            }
        } catch (IOException ioe) {
        }
    }

